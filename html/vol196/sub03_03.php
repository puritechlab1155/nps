                <p class="ptxt mt30">평범함을 거부하는 콘셉트에서 음식점도 예외는 아니다. 호주의 대화형 식당 
                    ‘카렌스다이너’(Karen’s Diner)는 ‘세상에서 가장 무례한 곳’이라는 콘셉트를 내세운다. 손님을 가볍게 무시하는 건 기본, 스마트폰으로 게임을 하거나 주문 손님에게 짜증을 내는 건 선택일 정도로 안하무인이지만, 손님들은 되레 재미있고 신선하다고 평가한다. 이런 콘셉트에 열광하는 젊은 세대들의 인기를 등에 업고 이 식당은 전 세계 11개가 넘는 지점을 두기도 했다.</p>
                <p class="ptxt mt30"><span class="bg_yellow">두 번째 ‘관계형 디깅’은 덕후를 스스로 밝히는 ‘덕밍아웃’을 통해 만나는 사람들끼리 서로 소통하고 특정 대상에 몰입하는 유형이다. BTS(방탄소년단)나 임영웅의 팬질이 대표적 사례다.</span> 이들의 디깅은 때론 새로운 콘텐츠 생성에도 기여한다. 지난해 ENA 드라마 ‘이상한 변호사 우영우’가 큰 인기를 끌자, JTBC가 운영하는 유튜브 채널에서 이 드라마 주인공인 박은빈과 강태오의 이전 출연작들을 짜깁기한 새로운 페이크(가짜) 드라마를 제작했다. 서로 다른 엉뚱한 장면들을 잘라 붙였지만, 대사가 자연스러워 ‘당장 방영해도 될 수준’이라는 찬사까지 받았다. 디깅러들의 몰입이 어떤 효과를 일으키는지 보여주는 사례로 통했다.</p>
                <p class="ptxt mt30"><span class="bg_yellow">마지막 ‘수집형 디깅’은 말 그대로 물건이나 캐릭터를 수집하는 유형이다. 세븐일레븐은 지난해 포켓몬, 짱구, 산리오 캐릭터로 구성된 ‘캐릭터 마이키링 3종’을 출시해 두 달 만에 누적 판매량 200만 개를 돌파했다. 레고의 경우 레테크(레고+재테크)까지 등장했다.</span> 영국 일간지 텔레그래프는 지난 15년간 출시된 한정판 레고 세트의 중고 가격 상승률과 금·주식 투자 수익률을 비교한 결과 레고의 수익성이 훨씬 좋다고 결론 냈다.
                    뮤지컬처럼 물건이 아닌 경험을 수집하는 디깅 사례도 적지 않다. 인터파크가 지난해 뮤지컬 관객 중 N차 관람 관객을 조사했더니, 2021년 한 해 같은 작품을 가장 많이 반복해서 본 관객의 관람 횟수가 86회였고 뒤이어 77회와 72회였다.</p>


