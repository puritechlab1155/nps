				<h3 class="h307 mt60">현실에 충실한 개방적 <span class="text_orange">·열정적인 덕후들의 세상</span></h3>
                <p class="ptxt"><span class="bg_yellow">디깅의 유형이 콘셉트이든 관계형이든 수집형이든 이들에게 보이는 하나의 공통점은 현실 도피적이지 않다는 것이다. 오타쿠나 덕후 개념에선 은둔형 외톨이나 폐쇄적 몰입이 주요 부정적 특징으로 설명되곤 했는데, 디깅 문화에선 되레 개방적이면서 자랑질이 넘치고 독특함에서 더 특별함을 느낀다. 자신이 좋아하고 사랑하는 일에 진심인 데다, 열정적이라는 점에서 희망적이기까지 하다.</span> 디깅이 어떤 특정 세대에 국한한 한계적 취미 현상으로 보는 것도 선입견에 불과하다. 코난테크놀로지가 디깅, 과몰입, 팬덤 등의 키워드 언급량을 통해 조사한 결과를 보면 2030세대의 디깅 관심이 4050세대보다 높지만, 4050세대 관심 역시 꾸준히 증가하고 있다는 것을 알 수 있다. 2030세대는 특히 다른 어떤 세대보다 가상 세계를 또 하나의 현실로 쉽게 받아들일 수 있는 환경적 영향으로 몰입의 적응도가 빠른 것으로 나타났다.<br>
                    <span class="bg_yellow">한 치 앞을 내다보기 힘든 불안한 미래를 마주할수록 잠깐의 몰입이 주는 행복감은 내일의 스트레스나 불안을 이기는 활력소가 될 수 있다.</span> 나아가 한 번도 경험하지 못한 새로운 창조의 세계로 한발 디디는 효과까지 노려볼 수도 있다.<br>
