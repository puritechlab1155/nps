					<p class="ptxt mt40">금오산 절벽에 자리한 압도적인 전경의 향일암. 눈앞이 아득해질 만큼 수려한 해상경관과 거친 기암괴석 절벽, 그 위에서 고고한 자태를 뽐내는 향일암의 모습은 신비로움을 넘어 경외감마저 들게 한다. 향일암은 644년 신라의 원효대사가 창건했다고 전해진다. 신라시대에는 원통암으로, 고려시대에는 금오암으로 불리다 조선 숙종 41년에 인묵대사가 향일암이라 개칭했다. ‘절벽에 세워져 해를 향하는 임자’라는 뜻으로 남해 수평선 너머 일몰이 장관을 이룬다. 바다와 산이 맞닿은 향일암은 간절히 기도하면 소원 한 가지를 이뤄준다는 전국 4대 관음 기도처 중 한 곳. 시간 맞춰 올라가면 환상적인 해돋이를 감상할 수 있다.</p>
