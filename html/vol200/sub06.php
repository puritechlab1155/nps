					<br class="pc_display">
					여성이 임신과 출산을 꺼리는 원인 중의 하나는 ‘경력 단절’이다. 출산부터 유아기까지 아이 곁을 지키다 보면 몇 년의 시간이 훌쩍 흐른다. 동료들이 일터에서 차곡차곡 경력을 쌓는 동안 출산과 육아로 생긴 공백은 자신의 커리어에 치명적일 수 있다. 과거에 비해 많이 개선됐다지만, 여전히 출산 휴가와 육아 휴가에 부정적인 일터도 적지 않다. 여성 개인에게 손실이라는 생각이 드는 한, 여성이 임신과 출산을 흔쾌히 결정하기는 쉽지 않다.  <br>
					인간은 누구나 자신에게 이익이 되는 방향으로 의사결정을 한다. 그런 점에서 임신과 출산에 대해 제도적으로 지원하는 것은 중요하다. 국민연금은 출산과 양육으로 인한 경력 단절로 연금 가입이 중단되는 것에 대한 대책으로 출산 크레딧을 마련했다. 출산 크레딧은 2008년 1월 1일 이후 둘째 자녀 이상을 갖는 경우 국민연금의 가입기간을 추가로 인정해 주는 제도다. 둘째 자녀 12개월, 셋째 자녀 30개월, 넷째 자녀 48개월, 다섯째 자녀 이상은 50개월을 추가로 인정해 주는 만큼 향후 받을 수 있는 연금은 더 많아진다. <br>
					출산 크레딧은 출산 후 곧바로 인정되지 않는다. 국민연금 가입자가 수급권을 취득하고(가입기간 추가 시 수급권을 취득할 수 있는 경우 포함) 노령연금을 받을 시기가 됐을 때 추가 가입기간이 반영된 연금을 받을 수 있다. 연금 수령자가 사전에 별도로 국민연금에 신청할 필요가 없다는 뜻이다. <br>
					출산 크레딧에 따른 국민연금 추가 가입기간은 부모 양쪽에게 균등하게 부여된다. 다만 합의한다면 한 사람이 추가 가입기간을 모두 가져갈 수 있다. 이 경우 부모 중 한 명이 먼저 연금 지급을 청구한 날로부터 1개월 이내에 합의서를 제출해야 한다. <br>
					통상적으로 저출생 지원은 출산과 육아에 집중돼 있다. 그런 점에서 노후에 도움을 주는 출산 크레딧은 의미가 크다. 공공이 출산에 대해 끝까지 지원한다는 시그널을 줄 수 있기 때문이다.
