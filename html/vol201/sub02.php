					국민연금법 제1조는 ‘국민의 노령, 장애 또는 사망에 대하여 연금급여를 실시함으로써 국민의 생활 안정과 복지 증진에 이바지하는 것을 목적으로 한다’이다. 은퇴 후 수급 연령에 도달했을 때 ‘노령연금’을, 가입기간에 장애를 입었을 때는 ‘장애연금’을, 가입 중 사망했을 때는 뒤에 남은 유족을 위해 ‘유족연금’을 각각 지급하는 등 국민 전체의 노후 소득을 책임지는 것이야말로 최우선 목적이자 존재 이유라고 명확하게 밝힌 셈이다. 또한 국민연금법 제4조는 ‘급여 수준과 연금보험료는 국민연금 재정이 장기적으로 균형을 유지할 수 있도록 해야 한다’라며 제도의 지속성을 강조했다.<br>
					하지만 국민연금은 납부한 보험료보다 연금급여를 훨씬 많이 받는 구조여서 급격한 저출생과 고령화에 따른 인구구조의 급변을 고려할 때 지속가능성이 낮다. 그렇다면 국민연금이 다층 노후 소득 보장 구조에서 중추 역할을 하게 하려면 어떻게 해야 할까?<br>
					국민연금, 즉 노령연금의 보장성이 떨어지는 주된 이유는 흔히 소득대체율로 불리는 지급률이 낮아서가 아니다. 40년 가입 기준 지급률 40%는 소득의 9%에 불과한 우리나라 보험료율을 감안할 때 다른 연금 선진국들보다 절대로 뒤떨어지지 않는다. 다만, 1988년 도입된 국민연금이 아직 충분히 무르익지 않은 데다, 전 국민을 포괄하게 된 게 2000년 이후인 탓에, 현세대 노인 중에는 수급권이 없는 사람이 많고, 수급권이 있더라도 보험료를 납부한 실질 가입 기간이 짧아 수급액이 적다.
