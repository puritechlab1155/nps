					국민연금을 받는 비율, 즉 수급률이 낮은 것도 연금 수급을 위한 최소 가입기간(120개월)을 채우지 못해서 벌어졌다는 점에서 결국 모든 문제는 가입기간이 짧다는 데로 모인다.<br>
					우리나라 국민연금 가입기간은 2019년 기준으로 평균 17.8년에 불과하다. 핀란드, 스웨덴, 프랑스, 오스트리아, 벨기에, 이탈리아, 스페인, 포르투갈 등 유럽 8개국의 평균 가입기간이 36.1년에 달하는 것과 비교할 때 19년 짧다. 2060년 우리나라 국민연금 신규 수급자의 예상 가입 기간도 약 25년에 그칠 것으로 추산한다.<br>
					우리나라 국민연금의 가입기간이 짧은 이유는 먼저 의무 가입 상한 연령 자체가 만 59세로 묶여 있어 다른 선진국보다 5년 이상 짧은 데다가 가입기간에 실직, 파산 등으로 보험료를 내지 않은 기간이 길기 때문이다. 따라서 많은 전문가는 국민연금의 노후 소득 보장 기능을 강화하려면 무엇보다 가입기간 문제를 해결해야 한다고 말한다.<br>
					가입기간을 확충하기 위해서는 현행 가입 상한 연령(만 59세)을 수급 개시 연령에 맞춰 만 64세로 상향 조정하고, 군복무·출산·실업크레딧을 확대하며, 저소득층 보험료 지원사업을 더욱 적극적으로 벌여야 한다. 전문가들은 이에 더 나아가 누구나 18세가 되면 국민연금에 자동으로 가입되도록 제도를 개선할 필요가 있다고 제안한다.<br>
					현재 국민연금 의무 가입 대상은 소득이 있는 만 18세 이상 60세 미만의 모든 국민이다. 학생, 군인이라도 소득이 있으면 18세부터 국민연금에 가입해야 하지만 소득이 없으면 27세 이전까지는 적용 제외에 해당한다. 27세 이후에는 전업주부 등 무소득 배우자만 적용 제외 대상이 되고, 나머지는 소득이 없더라도 납부 예외 대상으로 전환된다.<br>
					적용 제외, 납부 예외에 해당해 20대에 소득이 없어서 보험료를 내지 않더라도 나중에 소득이 생길 때 추후 납부(추납) 제도를 통해 더 쉽게 가입기간을 늘릴 수 있다.
