				<h4>노령연금 수급 기회를 확대하는 실업 크레딧</h4>
				<br class="pc_display">
				<div class="text">
					화려했던 젊은 시절이 반드시 노년을 보장하지는 않는다. 퇴직과 가족과의 이별은 때로 전혀 다른 삶으로 이끌기도 한다. 특히 퇴직 후 수입이 끊기게 되면 가난이라는 불청객이 노년을 괴롭힐 수 있다. 청년 때와 달리 노년에 가질 수 있는 직업은 한정되고 제한돼 있다. 이럴 때 필요한 것이 사회보장제도이다.<br>
					국민연금은 노후대비를 위한 대표적인 사회보장제도다. 연금을 든든히 받으려면 보험료는 많이, 납입 기간은 길게 가져갈수록 유리하다. 만약 실직을 당해 수입이 없어 보험료를 납입하지 못했다면 추후 받게 되는 국민연금 수령액이 상대적으로 낮아진다. 이 같은 문제점을 보완하기 위해 국민연금이 도입한 제도가 ‘실업 크레딧’이다. 실업 크레딧은 만 18세 이상 60세 미만의 ‘구직급여 수급자(실업급여 수급자)’가 국민연금 가입기간 추가를 희망하는 경우 1인 생애 최대 12개월까지 국가가 국민연금 보험료의 75%를 지원하고, 그 기간만큼 국민연금 가입 기간을 추가로 인정해 주는 제도다. 실업 크레딧 보험료는 인정소득에 보험요율(9%)을 곱해 구한다. 인정소득이란 실직 직전 3개월간 평균소득의 50%에 해당하는 금액으로 최대 70만 원을 초과할 수 없다.<br>
					예컨대 실직 전 3개월간 평균소득이 120만 원이라면 50%인 60만 원이 인정소득이 된다. 인정소득에 보험료율(9%)을 곱하면 5만 4000원인데 이 중 75%(4만 500원)를 정부가 부담하고 가입자는 25%(1만 3500원)를 낸다. 만약 실직 전 3개월간 평균소득이 200만 원이라고 하면 50%인 100만 원이 인정소득이 되지만, 상한 규정(최대70만 원)에 따라 인정소득은 70만 원이다. 인정소득에 보험료율(9%)을 곱한 6만 3000원이 납부해야 할 보험료로 이 중 정부가 75%(4만 7250원), 개인이 25%(1만 5750원)를 각각 납입한다.<br>
					실업 크레딧 지원 대상은 구직급여 수급자 중 국민연금 보험료를 1개월 이상 납부한 이력이 있는 가입자(가입자였던 자 포함)가 재산 및 소득 기준을 충족하는 경우다. 재산 기준은 재산세 과세표준의 합이 6억 원 이하, 소득 기준은 연간 종합소득 1,680만 원 이하다.<br>
					실업 크레딧은 매번 구직급여를 받을 때마다 가입할 수 있는 게 아니다. 구직급여를 몇 번이나 받든 합쳐서 생애 최대 12개월까지만 지원받는다. 즉 A회사 퇴직 후 실업 크레딧 8회를 지원받았다면, B회사 입사 후 퇴직 시 4회만 실업 크레딧을 받을 수 있다.<br>
					실업 크레딧은 고용복지플러스센터에서 신청할 수 있지만, 만약 구직급여를 받고 있다면 구직급여 종료일이 속하는 달의 다음 달 15일까지 국민연금공단 지사를 통해 신청하면 된다. 실업 크레딧으로 연금보험료 납입액이 많아지고 납입기간이 길어지면 추후 수급 때 더 많은 연금을 받을 수 있다.
