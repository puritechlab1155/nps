				<br class="pc_display">
				<div class="text">
					윌은 사고로 척수를 다쳐 하반신이 마비됐다. 척수 손상으로 사지 마비가 된 환자들은 갑작스러운 두통과 함께 어지럽고 열이 나면서 혈압이 오르고 식은땀이 흐르는 자율신경 반사부전을 겪곤 한다. 혈압이 불안정하므로 감염이 쉽게 돼 폐렴에도 취약하다. 문제는 척추 외상의 경우 별다른 치유법이 없다는 것. 그저 상황이 악화하지 않기만 바라는 것이 전부다. <br>
					윌처럼 노동력을 영구 상실하는 사고를 당하면 일반적으로 생계유지가 어려워진다. 이때 국민연금은 국민연금 가입자의 안정적인 생활을 돕기 위해 장애연금을 지급한다. 국민연금 가입자 혹은 가입자였던 사람이 질병이나 부상으로 인해 완치 후에도 신체적, 정신적 장애가 남았을 때 국민연금이 지급하는 연금을 국민연금 장애연금이라고 한다. 척수 손상처럼 완치되지 않는 장애를 입었을 경우는 초진일로부터 1년 6개월이 경과된 이후부터 장애연금이 지급된다.<br>
					급여는 장애정도(1~4급)에 따라 차등 지급된다. 장애1급은 예상 노령연금의 100%, 장애2급은 80%, 장애3급은 60%를 연금으로 받는다. 4급은 일시금 형태로 지급된다. 다만 장애등급은 국민연금이 자체적으로 부여한 등급으로 복지카드에 기재된 장애등급과는 다르다.<br>
					장애연금 수급을 위해서는 초진일 요건과 연금보험료 납부 요건을 모두 충족해야 한다. 예컨대 초진일이 만 18세 생일부터 노령연금 지급개시 연령 사이에 있어야 하고 초진일 당시 연금보험료 납부기간이 가입대상기간의 3분의 1이상이 되거나 10년 이상이 돼야 한다.<br>
					장애연금 수급자와 수급액은 매년 증가하고 있다. 2023년에는 78,527명에게 4,749억 원이 장애연금으로 지급됐다. 수급자를 연령별로 보면 50대가 28,561명으로 가장 많았고 이어 60~69세가 21,883명으로 뒤를 이었다. 2023년 신규 수급자의 장애연금 개시 시기는 평균 50.2세로 집계됐다.<br>
					장애연금 월평균 수령액도 증가하고 하고 있다. 2023년 기준 월평균 50만 5,000원이 지급된다. 2021년은 45만 8,236원, 2021년은 46만 1,510원, 2022년은 47만 4,879원이었다. 장애연금 월 최고 수령액은 222만 8,000원, 최장기 수급기간은 34년 10개월로 집계됐다. 1988년 제도 시행 이후 지금까지 29만 4,793명에게 7조 7,955억 원이 지급됐다. 장애연금이 장애로 인한 노동력 상실에 따른 구제를 일정 부분 담당하고 있다는 의미다.<br>
					국민연금 장애연금은 장애인연금법에 따르는 장애인연금과는 다른 연금이다. 국민연금은 소득과 관계없이 연금가입자에게 지급되지만, 장애인연금은 일정 소득 이하인 경우만 지급된다. 장애연금은 국민연금에, 장애인연금은 행정복지센터에 신청할 수 있다. 만약 총소득이 적다면 장애연금과 장애인연금을 동시 수령할 수도 있다.
